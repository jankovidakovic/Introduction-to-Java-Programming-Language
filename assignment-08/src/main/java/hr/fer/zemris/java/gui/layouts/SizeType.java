package hr.fer.zemris.java.gui.layouts;

/**
 * Enumeration for all the different size types of graphical components.
 * 
 * @author jankovidakovic
 *
 */
public enum SizeType {

	/**
	 * Corresponds to the preferred size of some component
	 */
	PREFERRED,
	/**
	 * Corresponds to the minimum size of some component
	 */
	MINIMUM,
	/**
	 * Corresponts to the maximum size of some component.
	 */
	MAXIMUM;
}
